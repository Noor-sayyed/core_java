package day2.Methods;
//static methods
public class Ex1 {
	public static void add() {
		int x = 1;
		int y = 2;
		int z = x+y;
		System.out.println(z);
	}
	
	public static void sub() {
		int x = 10;
		int y = 2;
		int z = x-y;
		System.out.println(z);
	}
public static void main(String[] args) {
	add();
	sub();
}
}
